text = "Ini adalah contoh string"

#Ubah semua huruf jadi kapital

kapital = text.upper()
print(kapital)

#ubah semua jadi huruf kecil

kecil = text.lower()
print(kecil)

# Huruf pertama Besar
awal = text.capitalize()
print(awal)

#huruf awal setiap kata jadi besar

judul = text.title()
print(judul)

